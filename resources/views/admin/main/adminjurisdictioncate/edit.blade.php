        <script>
            layui.use(['form','layer'], function(){
                $ = layui.jquery;
              var form = layui.form()
              ,layer = layui.layer;
            

              //监听提交
              form.on('submit(save)', function(data){
//                console.log(data);
                //发异步，把数据提交给php
                  $.ajax({
                      url:'{{ url('admin/adminjurisdiction') }}' + '/'+ data.field.id,
                      type:'put',
                      datatype:'json',
                      data: {
                          '_token':"{{ csrf_token() }}",
                          'id':data.field.id,
                          'class_name':data.field.class_name
                      },
                      success:function (data){
                          if (data == '1') {
                              layer.alert("保存成功", {icon: 6},function () {
                                  // 获得frame索引
                                  var index = parent.layer.getFrameIndex(window.name);
                                  //关闭当前frame
                                  parent.layer.close(index);
                              });

                          } else {
                              layer.alert("保存失败", {icon: 5},function () {
                                  // 获得frame索引
                                  var index = parent.layer.getFrameIndex(window.name);
                                  //关闭当前frame
                                  parent.layer.close(index);
                              });
                          }
                      }
                  });

                return false;
              });
              
              
            });
        </script>
